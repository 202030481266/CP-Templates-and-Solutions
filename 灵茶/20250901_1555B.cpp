static constexpr int maxn = 2e5 + 7;
static constexpr int inf = 0x3f3f3f3f;

int main() {
	int t;
	read(t);
	while (t--) {
		int W, H, x1, y1, x2, y2, w, h, ans = inf;
		read(W); read(H), read(x1); read(y1); read(x2); read(y2); read(w); read(h);
		if (abs(x2 - x1) + w > W && abs(y2 - y1) + h > H) {
			writeln(-1);
		}
		else {
			if (h + abs(y2 - y1) <= H) {
				ans = min(ans, max(0, h - min(y1, y2)));
				ans = min(ans, max(0, max(y1, y2) - (H - h)));
			}
			if (w + abs(x2 - x1) <= W) {
				ans = min(ans, max(0, w - min(x1, x2)));
				ans = min(ans, max(0, max(x1, x2) - (W - w)));
			}
			writeln(ans);
		}
	}
}