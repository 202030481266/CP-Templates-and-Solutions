using bs = bitset<N>;

void solve() {
	int n;
	cin >> n;
	vl arr(n + 1), sum(n + 1);
	for (int i = 1; i <= n; ++i) cin >> arr[i];
	for (int i = 1; i <= n; ++i) sum[i] = sum[i - 1] ^ arr[i];
	vec<bs> dp(n + 1);
	vl f(n + 1), can(n + 1);
	dp[1].set(n);
	for (int i = 1; i <= n; ++i) {
		ll mask = 0, zero = 0;
		for (int j = n; j >= i; --j) {
			ll x = sum[j] ^ sum[i - 1];
			int h = 63 - countl_zero(static_cast<ull>(x));
			if (!dp[i].test(j)) {
				if (zero) dp[i].set(j);
				else {
					if (mask & x || f[j] & x || can[j]) dp[i].set(j);
				}
			}
			if (dp[i].test(j)) {
				if (x) f[j] |= 1ll << h;
				else can[j] = 1;
				if (x == 0) zero = 1;
				else mask |= 1ll << h;
			}
		}
	}
	for (int i = 1; i <= n; ++i) cout << (dp[i].test(i) ? 1 : 0);
	cout << '\n';
}


int main() {
	ios::sync_with_stdio(false);
	cin.tie(nullptr);
	int t = 1;
	cin >> t;
	while (t--) solve();
}