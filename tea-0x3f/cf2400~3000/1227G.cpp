void solve() {
	int n;
	cin >> n;
	vi arr(n);
	for (int& v : arr) cin >> v;
	vi b(n);
	iota(b.begin(), b.end(), 0);
	ranges::sort(b, [&](int i, int j) {return arr[i] < arr[j]; });
	vii ans(n + 2, vi(n + 1));
	for (int i = 1; i <= n; ++i) {
		int v = arr[b[i - 1]];
		int p = i;
		while (p >= 1 && v) {
			ans[p][i] = 1;
			--p;
			--v;
		}
		p = i + 2;
		while (p <= n + 1 && v) {
			ans[p][i] = 1;
			++p;
			--v;
		}
	}
	vi ref(n);
	for (int i = 0; i < n; ++i) ref[b[i]] = i;
	cout << n + 1 << '\n';
	for (int i = 1; i <= n + 1; ++i) {
		for (int j = 0; j < n; ++j) cout << ans[i][ref[j] + 1];
		cout << '\n';
	}
}


